public class Herec {
    private String jmeno;
    private int rok;

    public Herec(String jmeno, int rok) {
        this.jmeno = jmeno;
        this.rok = rok;
    }


    public String getJmeno() {
        return jmeno;
    }
    public void setJmeno(String jmeno) {
        this.jmeno = jmeno;
    }
    public int getRok() {
        return rok;
    }
    public void setRok(int rok) {
        this.rok = rok;
    }

}
